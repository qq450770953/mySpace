def update_user(user_id):
    """更新用户信息 - 可由其他模块导入的功能函数"""
    try:
        current_app.logger.info(f"开始更新用户 ID: {user_id}")
        
        # 获取请求数据并记录
        data = request.get_json()
        current_app.logger.info(f"更新用户请求数据: {data}")
        
        # 检查用户是否存在
        user = User.query.get_or_404(user_id)
        current_app.logger.info(f"找到用户: {user.username}, ID: {user.id}")
        
        # 更新基本信息
        if 'name' in data:
            current_app.logger.info(f"更新用户名: {data['name']}")
            user.name = data['name']
            
        if 'username' in data and data['username'] != user.username:
            current_app.logger.info(f"更新登录名: {data['username']}")
            # 检查用户名是否已被使用
            if User.query.filter(User.username == data['username'], User.id != user_id).first():
                current_app.logger.warning(f"用户名已被使用: {data['username']}")
                return jsonify({'error': '用户名已被使用'}), 400
            user.username = data['username']
            
        if 'email' in data and data['email'] != user.email:
            current_app.logger.info(f"更新邮箱: {data['email']}")
            # 检查邮箱是否已被使用
            if User.query.filter(User.email == data['email'], User.id != user_id).first():
                current_app.logger.warning(f"邮箱已被使用: {data['email']}")
                return jsonify({'error': '邮箱已被使用'}), 400
            user.email = data['email']
            
        if 'is_active' in data:
            current_app.logger.info(f"更新状态: {data['is_active']}")
            user.is_active = data['is_active']
        
        # 如果提供了新密码，更新密码
        if 'password' in data and data['password']:
            current_app.logger.info("更新密码")
            user.set_password(data['password'])
        
        # 处理角色更新
        try:
            # 如果提供了角色，更新角色
            if 'role' in data:
                current_app.logger.info(f"更新角色: {data['role']}")
                
                # 验证角色是否存在
                role = Role.query.filter_by(name=data['role']).first()
                if not role:
                    current_app.logger.warning(f"角色不存在: {data['role']}")
                    return jsonify({'error': f'角色不存在: {data["role"]}'}), 400
                
                # 清除现有角色
                current_app.logger.info("清除现有角色")
                user.roles = []
                
                # 添加新角色
                current_app.logger.info(f"添加新角色: {role.name}")
                user.roles.append(role)
                
        except Exception as role_error:
            current_app.logger.error(f"更新角色时出错: {str(role_error)}")
            return jsonify({'error': f'更新角色时出错: {str(role_error)}'}), 500
        
        # 更新时间戳并提交
        user.updated_at = datetime.now(pytz.utc)
        current_app.logger.info("提交用户更新到数据库")
        db.session.commit()
        
        current_app.logger.info(f"用户 {user.username} 更新成功")
        return jsonify({
            'message': '用户信息更新成功',
            'user': user.to_dict()
        })
        
    except Exception as e:
        db.session.rollback()
        current_app.logger.error(f"更新用户信息出错: {str(e)}", exc_info=True)
        return jsonify({'error': f'更新用户信息失败: {str(e)}'}), 500

