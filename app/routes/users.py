@users_bp.route('/<int:user_id>', methods=['PUT'])
@jwt_required(locations=['headers', 'cookies', 'query_string'])
@permission_required(PERMISSION_MANAGE_USERS)
def update_user_route(user_id):
    """"""
    return update_user(user_id)

def update_user(user_id):
    """ - """
    try:
        current_app.logger.info(f" ID: {user_id}")
        
        # 
        data = request.get_json()
        current_app.logger.info(f": {data}")
        
        # 
        user = User.query.get_or_404(user_id)
        current_app.logger.info(f": {user.username}, ID: {user.id}")
        
        # 
        if 'name' in data:
            current_app.logger.info(f": {data['name']}")
            user.name = data['name']
            
        if 'username' in data and data['username'] != user.username:
            current_app.logger.info(f": {data['username']}")
            # 
            if User.query.filter(User.username == data['username'], User.id != user_id).first():
                current_app.logger.warning(f": {data['username']}")
                return jsonify({'error': ''}), 400
            user.username = data['username']
            
        if 'email' in data and data['email'] != user.email:
            current_app.logger.info(f": {data['email']}")
            # 
            if User.query.filter(User.email == data['email'], User.id != user_id).first():
                current_app.logger.warning(f": {data['email']}")
                return jsonify({'error': ''}), 400
            user.email = data['email']
            
        if 'is_active' in data:
            current_app.logger.info(f": {data['is_active']}")
            user.is_active = data['is_active']
        
        # 
        if 'password' in data and data['password']:
            current_app.logger.info("")
            user.set_password(data['password'])
        
        # 
        try:
            # 
            if 'role' in data:
                current_app.logger.info(f": {data['role']}")
                
                # 
                role = Role.query.filter_by(name=data['role']).first()
                if not role:
                    current_app.logger.warning(f": {data['role']}")
                    return jsonify({'error': f': {data["role"]}'}), 400
                
                # 
                current_app.logger.info("")
                user.roles = []
                
                # 
                current_app.logger.info(f": {role.name}")
                user.roles.append(role)
                
        except Exception as role_error:
            current_app.logger.error(f": {str(role_error)}")
            return jsonify({'error': f': {str(role_error)}'}), 500
        
        # 
        user.updated_at = datetime.now(pytz.utc)
        current_app.logger.info("")
        db.session.commit()
        
        current_app.logger.info(f" {user.username} ")
        return jsonify({
            'message': '',
            'user': user.to_dict()
        })
        
    except Exception as e:
        db.session.rollback()
        current_app.logger.error(f": {str(e)}", exc_info=True)
        return jsonify({'error': f': {str(e)}'}), 500

