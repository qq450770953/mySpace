@users_bp.route('/login', methods=['POST'])
def login():
    data = request.get_json()
    user = User.query.filter_by(email=data['email']).first()
    if not user or not check_password_hash(user.password, data['password']):
        return jsonify({'error': 'Invalid email or password'}), 401
    
    token = generate_token(user)
    return jsonify({
        'token': token,
        'user': user.to_dict()
    })

@users_bp.route('/', methods=['GET'])
@jwt_required(locations=['headers', 'cookies', 'query_string'])
@permission_required(PERMISSION_MANAGE_USERS)
def list_users():
    """获取用户列表"""
    try:
        # 获取分页参数
        page = request.args.get('page', 1, type=int)
        per_page = request.args.get('per_page', 10, type=int)
        
        # 查询用户列表
        pagination = User.query.paginate(page=page, per_page=per_page)
        users = pagination.items
        
        # 返回用户列表和分页信息
        return jsonify({
            'users': [user.to_dict() for user in users],
            'total': pagination.total,
            'pages': pagination.pages,
            'current_page': page,
            'per_page': per_page
        })
        
    except Exception as e:
        current_app.logger.error(f"获取用户列表出错: {str(e)}")
        return jsonify({'error': '获取用户列表失败'}), 500

@users_bp.route('/', methods=['POST'])
@jwt_required(locations=['headers', 'cookies', 'query_string'])
@permission_required(PERMISSION_MANAGE_USERS)
def create_user():
    """创建新用户"""
    try:
        data = request.get_json()
        
        # 校验必填字段
        required_fields = ['username', 'email', 'password']
        for field in required_fields:
            if field not in data:
                return jsonify({'error': f'缺少必填字段 {field}'}), 400
        
        # 校验用户名和邮箱是否已存在
        if User.query.filter_by(username=data['username']).first():
            return jsonify({'error': '用户名已存在'}), 400
            
        if User.query.filter_by(email=data['email']).first():
            return jsonify({'error': '邮箱已存在'}), 400
        
        # 创建新用户
        user = User(
            username=data['username'],
            email=data['email'],
            name=data.get('name', data['username']),
            created_at=datetime.now(pytz.utc)
        )
        user.set_password(data['password'])
        
        # 分配角色
        role_name = data.get('role', 'user')
        role = Role.query.filter_by(name=role_name).first()
        if role:
            user.roles.append(role)
        else:
            # 如果指定的角色不存在，默认分配用户角色
            default_role = Role.query.filter_by(name='user').first()
            if default_role:
                user.roles.append(default_role)
        
        db.session.add(user)
        db.session.commit()
        
        return jsonify({
            'message': '创建用户成功',
            'user': user.to_dict()
        }), 201
        
    except Exception as e:
        db.session.rollback()
        current_app.logger.error(f"创建用户出错: {str(e)}")
        return jsonify({'error': '创建用户失败'}), 500

